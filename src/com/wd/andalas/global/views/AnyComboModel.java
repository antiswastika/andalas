package com.wd.andalas.global.views;

public class AnyComboModel {
	private String key;
	private String value;

	public AnyComboModel(String key, String value) {
	      this.key = key;
	      this.value = value;
	    }

	public String getKey() {
		return key;
	}

	public String getValue() {
		return value;
	}
}
